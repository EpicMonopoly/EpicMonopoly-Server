    BLUE = 0x3399ff
    RED = 0xbb0407
    YELLOW = 0xfdb515
    PURPLE = 0xbf3eff
    GREEN = 0x8dc63f
    DARK = 0x430a0a
    PINK = 0xff6666
    CYAN = 0x00FFFF
    TEAL = 0x008080
    DEEPPINK = 0xFF1493
    BROWN = 0xA52A2A
    ORANGE = 0xFF4500
